#include <iostream>

using namespace std;

int main()
{
    int t;
    cin >> t;

    while(t--)
    {
        int n;
        cin >> n;
        int a[n][n];
        int i=0;
        while(i<n)
        {
            for(int j=0; j<n; j++)
            {
                cin >> a[i][j];
            }
        }
        
    }
    return 0;
}